package sparseMatrix;

public class arrList {

	int data;
	int row;
	int col;
	
	public arrList(int data1, int row1, int col1) {
		data = data1;
		row = row1;
		col = col1;
	}

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}

	
	
	

}
